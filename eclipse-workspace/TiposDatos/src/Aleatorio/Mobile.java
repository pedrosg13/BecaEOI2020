package Aleatorio;

public class Mobile {
	
	private String imei;
	private String number;
	
	public Mobile() {
		super();
		this.imei = imei;
		this.number = number;
	}

	public String getImei() {
		return imei;
	}

	public void setImei(String imei) {
		this.imei = imei;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}
	
}
